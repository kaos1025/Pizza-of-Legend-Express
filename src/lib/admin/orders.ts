// Transform Supabase row to Order type
// eslint-disable-next-line @typescript-eslint/no-explicit-any
function transformDbOrder(row: Record<string, any>): Order {
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const items = (row.order_items || []).map((item: Record<string, any>) => ({
    id: item.id,
    type: item.menu_item?.category || 'pizza',
    name: {
      en: item.menu_item?.name_en || '',
      zh: item.menu_item?.name_zh || '',
      ja: item.menu_item?.name_ja || '',
    },
    size: item.size,
    quantity: item.quantity,
    unitPrice: item.unit_price,
  }));

  // Compute updated_at from status timestamps (DB has no updated_at column)
  const updated_at = row.cancelled_at
    || row.completed_at
    || row.delivering_at
    || row.confirmed_at
    || row.created_at;

  return {
    id: row.id,
    order_number: row.order_number,
    items,
    total_amount: row.total_amount,
    hotel_id: row.hotel_id,
    room_number: row.room_number,
    messenger_id: row.messenger_id || undefined,
    messenger_platform: row.messenger_platform || undefined,
    order_type: row.order_type || 'delivery',
    delivery_fee: row.delivery_fee || 0,
    special_request: row.special_request || undefined,
    status: row.status,
    payment_method: row.payment_method || undefined,
    language: row.language || 'en',
    created_at: row.created_at,
    updated_at,
    confirmed_at: row.confirmed_at || undefined,
    delivering_at: row.delivering_at || undefined,
    completed_at: row.completed_at || undefined,
    cancelled_at: row.cancelled_at || undefined,
  };
}

export function getTodaySettlement(orders: Order[]) {
  const completed = orders.filter(o => o.status === 'completed');
  const cash = completed.filter(o => o.payment_method === 'cash');
  const card = completed.filter(o => o.payment_method === 'card');
  const delivery = completed.filter(o => o.order_type === 'delivery');
  const pickup = completed.filter(o => o.order_type === 'pickup');

  return {
    totalRevenue: completed.reduce((s, o) => s + o.total_amount, 0),
    totalCount: completed.length,
    cashRevenue: cash.reduce((s, o) => s + o.total_amount, 0),
    cashCount: cash.length,
    cardRevenue: card.reduce((s, o) => s + o.total_amount, 0),
    cardCount: card.length,
    deliveryFeeTotal: completed.reduce((s, o) => s + (o.delivery_fee || 0), 0),
    deliveryCount: delivery.length,
    pickupCount: pickup.length,
  };
}

